int AlsaInput::start()
{
	int chCount = getParameter("channelCount").toInt();
	/* alsa defaults 48000 for invalid sample rates */
	int err = alsaIn->open(getParameter("audioRate").toInt(), chCount, Lmm::AUDIO_SAMPLE_S16, true);
	if (err)
		return err;
	bufferSize = getParameter("sampleCount").toInt() * chCount * alsaIn->getSampleSize();
	return BaseLmmElement::start();
}

int AlsaInput::stop()
{
	int err = alsaIn->close();
	if (err)
		return err;
	return BaseLmmElement::stop();
}
