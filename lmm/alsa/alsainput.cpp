int AlsaInput::processBlocking(int ch)
{
	Q_UNUSED(ch);
	if (getState() == STOPPED)
		return -EINVAL;
	if (isPassThru()) {
		sleep(1);
		return 0;
	}
	RawBuffer buf("audio/x-raw-int,", bufferSize);
	int err = alsaIn->read(buf.data(), bufferSize);
	/* alsa errors shouldn't stop our processing */
	if (err < 0) {
		mDebug("read error '%d' occurred, trying to continue", err);
		err = 0;
	}

	if (outputFormat == Lmm::CODEC_PCM_ALAW) {
		/* ASSUMPTION: input is 16-bit LE */
		const short *adata = (const short *)buf.constData();
		uchar *alaw = (uchar *)buf.data();
		for (int i = 0; i < buf.size() / 2; i++) {
			if (adata[i] > 0)
				alaw[i] = alawTable[adata[i] / 16];
			else
				alaw[i] = alawTable[adata[i] / -16] & 0x7F;
		}
		buf.setUsedSize(bufferSize / 2);
	}

	buf.pars()->encodeTime = streamTime->getCurrentTime();

	if (0) {
		static QElapsedTimer t;
		static int total = -1;
		if (total == -1) {
			t.start();
			total = 0;
		}
		total += buf.size();
		if (t.elapsed() > 1000) {
			if (outputFormat == Lmm::CODEC_PCM_ALAW)
				qDebug() << total * 1000 / t.elapsed() / alsaIn->channelCount();
			else
				qDebug() << total * 1000 / t.elapsed() / alsaIn->getSampleSize() / alsaIn->channelCount();
			total = 0;
			t.restart();
		}
	}

	if (getState() == STOPPED)
		return -EINVAL;
	if (err < 0)
		return err;
	return newOutputBuffer(ch, buf);
}

